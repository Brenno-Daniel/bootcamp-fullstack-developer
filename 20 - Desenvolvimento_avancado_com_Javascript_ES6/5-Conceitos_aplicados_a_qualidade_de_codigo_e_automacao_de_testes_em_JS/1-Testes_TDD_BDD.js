// Testes unitários; 
// Tem o papel de testar a menor unidade do código, de extrema importancia, 
// conceitos de TDD são mais voltados a unitários e integrados.

// Testes integrados;
// Testam a integração entre as pequenas partes que foram testadas anteriormente, é sobre integrar partes do
// seu próprio código entre si, ex: dois componentes ocom se comportam em conjunto, etc, ou seja, para garantir 
// que a integração dos contratos entre eles estejam sendo respeitados.


// Testes funcionais;
// Tambem conhecidos como end-to-end, testa integração do seu sistema com outros sistemas, 
// Testamos uma funcionalidade de negócio do começo ao fim. 
// Conhecido também como teste black box, pois testa como um usuário utilizando sem se importar 
// de como esta sendo executado por baixo, qual tecnologia esta sendo usada por baixo.
// Desse ponto de vista normalmente não fazemos o mock.


